    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // BOTON PARA ELIMINAR UNA VENTA
        try{
            if(jList1.getSelectedIndex()==-1 && jList2.getSelectedIndex()==-1){
                JOptionPane.showMessageDialog(this, "Error: Selecionar un valor de la lista.");
            }else{
                String[] botones = {"Si","No"};
                if(jList1.getSelectedIndex()!=-1){//seleciono ventas                   
                    int indexSelected = jList1.getSelectedIndex();
                    int valor = JOptionPane.showOptionDialog(this, "¿Esta seguro de eliminar "+ ventas.get(indexSelected) +" en Ventas?", 
                            "Advertencia", 0, 0, null, botones, this);
                    if(valor==JOptionPane.NO_OPTION) return; //Si da a NO se detiene todo y no elimina nada.
                    ventas.remove(indexSelected);
                    modelVenta.clear();
                    modelVenta.addAll(ventas);
                    String sumaVentas =  getSumaModel(modelVenta);
                    labelTotalVentas.setText(sumaVentas);
                    ArrayList<String> list = this.defaultListModelToArrayList(modelVenta);
                    this.documento.setPrecios(true, list);
                }else{//seleciono gastos.
                    int indexSelected = jList2.getSelectedIndex();
                    int valor = JOptionPane.showOptionDialog(this, "¿Esta seguro de eliminar "+ gastos.get(indexSelected) +" en Gastos?", 
                            "Advertencia", 0, 0, null, botones, this);
                    if(valor==JOptionPane.NO_OPTION) return; //Si da a NO, Se detiene todo y no elimina nada.
                    gastos.remove(indexSelected);
                    modelGasto.clear();
                    modelGasto.addAll(gastos);
                    String sumaVentas =  getSumaModel(modelGasto);
                    labelTotalGastos.setText(sumaVentas);
                    ArrayList<String> list = this.defaultListModelToArrayList(modelGasto);
                    this.documento.setPrecios(false, list);
                }
                this.documento.updateFormulas();
                this.documento.saveWordBook();
                this.documento = atras.newDocument(this.LabelFecha.getText());
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error: "+e.toString());
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

