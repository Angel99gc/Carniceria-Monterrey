    public void addPrecio(){
        try {
            String tipoPrecio = (String) jComboBox1.getSelectedItem();
            if(!jTextField1.getText().isEmpty()){
                int Precio = Integer.parseInt(jTextField1.getText());//precio digitado en el campo de text.
                if(tipoPrecio=="Venta"){
                    ventas.add(0,Precio);
                    modelVenta.clear();
                    modelVenta.addAll(ventas);
                    String sumaVentas =  getSumaModel(modelVenta);
                    labelTotalVentas.setText(sumaVentas);
                    ArrayList list = this.defaultListModelToArrayList(modelVenta);
                    this.documento.setPrecios(true, list);                
                }
                else if(tipoPrecio=="Gasto"){
                    gastos.add(0,Precio);
                    modelGasto.clear();
                    modelGasto.addAll(gastos);
                    String sumaGastos =  getSumaModel(modelGasto);
                    labelTotalGastos.setText(sumaGastos);
                    ArrayList list = this.defaultListModelToArrayList(modelGasto);
                    this.documento.setPrecios(false, list);
                }
                this.documento.updateFormulas();
                this.documento.saveWordBook();
                this.documento = atras.newDocument(this.LabelFecha.getText());
                jTextField1.setText("");//reinicia el campo de texto del precio
            }else{
                JOptionPane.showMessageDialog(this, "Error: Espacio vacio.");
            }
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: "+e.toString());
        }
            
    }
/*
    Suma todos los datos agregados al modelo y 
    retorna el string de la suma.
    */
    public String getSumaModel(DefaultListModel model){
        int sumaTotal = 0;
        for(int i=0; i < model.getSize(); i++){
            sumaTotal += Integer.parseInt(model.getElementAt(i).toString());
        }
        
        return String.valueOf(sumaTotal);
    }
    public ArrayList<String> defaultListModelToArrayList(DefaultListModel model){
        ArrayList<String> Lista = new ArrayList();
        for (int i = 0; i < model.getSize(); i++) {
            Lista.add(model.get(i).toString());
        }
        return Lista;
    }
