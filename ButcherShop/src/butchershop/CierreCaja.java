    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Guarda todos los cambios dados.
        if(inputMonedas.getText().isEmpty() || inputBilletes.getText().isEmpty() || inputTarjeta.getText().isEmpty() ){
            Border borderError = new LineBorder(Color.red,2);
            if(inputMonedas.getText().isEmpty())inputMonedas.setBorder(borderError);
            else inputMonedas.setBorder(new EtchedBorder());
            if(inputBilletes.getText().isEmpty())inputBilletes.setBorder(borderError);
            else inputBilletes.setBorder(new EtchedBorder());
            if(inputTarjeta.getText().isEmpty())inputTarjeta.setBorder(borderError);
            else inputTarjeta.setBorder(new EtchedBorder());
            JOptionPane.showMessageDialog(this, "Error: No puede haber espacios vacios.");
        }else{
            atras.documento.setCaja(atras.atras, Integer.parseInt(inputMonedas.getText()));
            atras.documento.setData("Billetes", Integer.parseInt(inputBilletes.getText()));
            atras.documento.setData("Monedas", Integer.parseInt(inputMonedas.getText()));
            atras.documento.setData("TotalTarjeta", Integer.parseInt(inputTarjeta.getText()));
            atras.documento.updateFormulas();
            atras.documento.saveWordBook();
            atras.documento = atras.atras.newDocument(fechaHoy);

            //cierra todo hasta regresar al menu principal
            atras.atras.setVisible(true);
            atras.dispose();
            
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

