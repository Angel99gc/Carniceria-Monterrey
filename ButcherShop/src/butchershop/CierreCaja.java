    //Funciones usadas en el cierre de caja
    public void updateView(){
        String billetes = this.inputBilletes.getText();
        String monedas = this.inputMonedas.getText();
        String tarjeta = this.inputTarjeta.getText();
        int caja = Integer.parseInt(this.labelCaja.getText());
        int b = 0;
        int m = 0;
        int t = 0;
        if(!billetes.isEmpty()){
            b = Integer.parseInt(billetes);
            if(!monedas.isEmpty()){
                m = Integer.parseInt(monedas);
                if(!tarjeta.isEmpty()){
                    t = Integer.parseInt(tarjeta);
                }
            }
        }
        this.labelEfectivo.setText(String.valueOf(b));
        this.totalRealizado.setText(String.valueOf((b+m)-caja));
        this.labelDiferencia.setText(String.valueOf(Integer.parseInt(this.totalRealizado.getText())-Integer.parseInt(this.totalSupuesto.getText())));
        this.labelTotalDeposito.setText(String.valueOf(b+t));
    }
    
    
    
